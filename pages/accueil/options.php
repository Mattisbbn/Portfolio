         <h2 class="section_title">Qu'est ce que le BTS SIO</h2>
         <span class="section_subtitle">//</span>
         <div class="option_container container">

             <div class="option_data">
                 <p class="option_description">
                     Le <b> Brevet de Technicien Supérieur aux Services Informatiques aux Organisations</b> (BTS SIO), s'adresse à ceux qui souhaitent se former en deux ans aux métiers d'administrateur réseau ou de développeur. Pour par la suite intégré directement le marché du travail ou continuer des études, dans le domaine de l'informatique.
                 </p>
             </div>


             <h2 class="option-subtitle">Le BTS SIO propose deux spécialités :</h2>
             <div class="option-grid">
                 <div>
                     <h3 class="option-h3">L'option SISR</h3>
                     <p class="option-text">
                         Le BTS SIO option SISR <b>(Solutions d'Infrastructure, Systèmes et Réseaux)</b>,
                         forme des professionnels spécialisés dans la conception, la mise en place et la gestion des infrastructures informatiques
                         et des réseaux au sein des entreprises.
                         <br><br>
                         Les étudiants développent des compétences en administration des systèmes,
                         virtualisation, sécurité des réseaux, gestion des services réseau, et support technique. 
                         <br><br>
                         Ce diplôme offre des débouchés
                         dans l'administration des systèmes et réseaux, la sécurité informatique, la gestion de parc informatique, ou encore le
                         déploiement de solutions réseau.
                     </p>
                 </div>
                 <div>
                     <h3 class="option-h3">L'option SLAM</h3>
                
                     <p class="option-text">
                        Le BTS SIO option SLAM <b> (Solutions Logicielles et Applications Métiers)</b>,
                         forme des professionnels capables de concevoir, développer et maintenir des solutions logicielles adaptées aux besoins
                         des entreprises.
                         <br><br>
                         Les étudiants acquièrent des compétences en développement d'applications, gestion de projet informatique,
                         bases de données, langages de programmation, sécurité des systèmes d'information, et technologies web.
                         <br><br>
                         Ce diplôme offre
                         des débouchés dans le développement d'applications, la gestion de projets informatiques, l'administration de systèmes et réseaux,
                         et le support technique
                     </p>
                 </div>
             </div>

         </div>
